#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*lstptr;

	if (!new || !lst)
		return ;
	if (! *lst)
	{
		*lst = new;
		return ;
	}
	lstptr = ft_lstlast(*lst);
	lstptr->next = new;
}
