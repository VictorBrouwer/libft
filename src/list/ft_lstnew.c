#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*linklist;

	linklist = malloc(sizeof(t_list));
	if (!linklist)
		return (NULL);
	linklist->content = content;
	linklist->next = NULL;
	return (linklist);
}
