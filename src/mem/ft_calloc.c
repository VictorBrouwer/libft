#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	char	*res;
	size_t	x;

	res = malloc(count * size);
	if (!res)
		return (NULL);
	x = 0;
	while (x < (count * size))
	{
		res[x] = '\0';
		x++;
	}
	return (res);
}
